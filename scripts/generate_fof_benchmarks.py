#!/usr/bin/env python3
"""
Improved FOF Benchmark Problem Generator

Generates multi-formula TPTP FOF problems (axioms + conjecture) designed to
discriminate between proof engines by varying:
  - Proof depth (implication chain length)
  - Branching factor (irrelevant distractor axioms)
  - Rule reuse (transitivity chains require repeated axiom application)
  - Quantifier structure (classical ∀/∃ alternation theorems)

Why these problems discriminate between engines:
  - Chain+distractor problems: priority scheduling helps by ranking productive
    rule applications over distractors; naive DFS tries them in arbitrary order.
  - Transitivity chains: iterative deepening (priority-id) handles deeper
    chains without over-committing to one branch early.
  - Unprovable problems: generated by removing a link (chain gap) or asking
    about an unrelated individual — provably CounterSatisfiable, not just
    tautology negations.

Usage:
    python scripts/generate_fof_benchmarks.py

Output: one .p file per problem in AI_generated/
    easy_1.p  .. easy_N.p
    medium_1.p .. medium_N.p
    hard_1.p  .. hard_N.p
    expert_1.p .. expert_N.p
"""

import os
from typing import List

Formula = List[str]


def write_problem(path: str, formulae: Formula) -> None:
    with open(path, 'w') as f:
        for line in formulae:
            f.write(line + '\n')


# ── Implication chain ────────────────────────────────────────────────────────
# p0(a), ∀X.(p0(X)=>p1(X)), ..., ∀X.(p{n-1}(X)=>pn(X))  ⊢  pn(a)
# Distractors are unrelated chains q0=>q1=>... that add irrelevant axioms.

def chain_provable(length: int, distractors: int) -> Formula:
    fmls = ["fof(fact, axiom, p0(a))."]
    for i in range(length):
        fmls.append(f"fof(step{i}, axiom, ! [X] : (p{i}(X) => p{i+1}(X))).")
    for d in range(distractors):
        fmls.append(f"fof(dist{d}, axiom, ! [X] : (q{d}(X) => q{d+1}(X))).")
    fmls.append(f"fof(goal, conjecture, p{length}(a)).")
    return fmls


def chain_unprovable(length: int, distractors: int, gap: int) -> Formula:
    """Chain with the link at position `gap` removed — proof is stuck."""
    fmls = ["fof(fact, axiom, p0(a))."]
    for i in range(length):
        if i == gap:
            continue
        fmls.append(f"fof(step{i}, axiom, ! [X] : (p{i}(X) => p{i+1}(X))).")
    for d in range(distractors):
        fmls.append(f"fof(dist{d}, axiom, ! [X] : (q{d}(X) => q{d+1}(X))).")
    fmls.append(f"fof(goal, conjecture, p{length}(a)).")
    return fmls


# ── Syllogism (class hierarchy) ──────────────────────────────────────────────
# c0 ⊆ c1 ⊆ ... ⊆ c_{depth+1}, c0(socrates)  ⊢  c_{depth+1}(socrates)

CLASSES = ['entity', 'organism', 'animal', 'mammal', 'primate',
           'human', 'person', 'scholar', 'student', 'teacher']


def syllogism_provable(depth: int) -> Formula:
    preds = CLASSES[:depth + 2]
    fmls = []
    for i in range(depth + 1):
        fmls.append(f"fof(isa{i}, axiom, ! [X] : ({preds[i]}(X) => {preds[i+1]}(X))).")
    fmls.append(f"fof(inst, axiom, {preds[0]}(socrates)).")
    fmls.append(f"fof(goal, conjecture, {preds[depth+1]}(socrates)).")
    return fmls


def syllogism_unprovable(depth: int) -> Formula:
    """Hierarchy proven for socrates; conjecture asks about unrelated plato."""
    preds = CLASSES[:depth + 2]
    fmls = []
    for i in range(depth + 1):
        fmls.append(f"fof(isa{i}, axiom, ! [X] : ({preds[i]}(X) => {preds[i+1]}(X))).")
    fmls.append(f"fof(inst, axiom, {preds[0]}(socrates)).")
    fmls.append(f"fof(goal, conjecture, {preds[depth+1]}(plato)).")
    return fmls


# ── Transitivity chain ───────────────────────────────────────────────────────
# trans axiom + rel(a0,a1), rel(a1,a2), ..., rel(a{n-1},an)  ⊢  rel(a0,an)
# Tests repeated reuse of a single axiom across many facts.

def trans_provable(steps: int, distractors: int) -> Formula:
    fmls = ["fof(trans, axiom, ! [X,Y,Z] : ((rel(X,Y) & rel(Y,Z)) => rel(X,Z)))."]
    for i in range(steps):
        fmls.append(f"fof(link{i}, axiom, rel(a{i}, a{i+1})).")
    for d in range(distractors):
        fmls.append(f"fof(dfact{d}, axiom, other(b{d}, b{d+1})).")
    fmls.append(f"fof(goal, conjecture, rel(a0, a{steps})).")
    return fmls


def trans_unprovable(steps: int) -> Formula:
    """Chain reaches a{steps}; conjecture asks for a{steps+2} — unreachable."""
    fmls = ["fof(trans, axiom, ! [X,Y,Z] : ((rel(X,Y) & rel(Y,Z)) => rel(X,Z)))."]
    for i in range(steps):
        fmls.append(f"fof(link{i}, axiom, rel(a{i}, a{i+1})).")
    fmls.append(f"fof(goal, conjecture, rel(a0, a{steps+2})).")
    return fmls


# ── Quantifier alternation (classical FOL theorems / non-theorems) ───────────

def q_flip_universal() -> Formula:
    return ["fof(goal, conjecture, (! [X] : ! [Y] : p(X,Y)) => (! [Y] : ! [X] : p(X,Y)))."]

def q_exists_implies() -> Formula:
    return ["fof(goal, conjecture, ? [Y] : (? [X] : p(X) => p(Y)))."]

def q_drinker() -> Formula:
    return ["fof(goal, conjecture, ? [Y] : (p(Y) => ! [X] : p(X)))."]

def q_converse_barcan() -> Formula:
    return ["fof(goal, conjecture, (? [Y] : ! [X] : p(X,Y)) => (! [X] : ? [Y] : p(X,Y)))."]

def q_pelletier18() -> Formula:
    return ["fof(goal, conjecture, ? [Y] : ! [X] : (f(Y) => f(X)))."]

def q_russell_diagonal() -> Formula:
    return ["fof(goal, conjecture, ~ ? [Y] : ! [X] : (r(X,Y) <=> ~ r(X,X)))."]

def q_barcan() -> Formula:
    return ["fof(goal, conjecture, (! [X] : ? [Y] : p(X,Y)) => (? [Y] : ! [X] : p(X,Y)))."]

def q_exists_to_forall() -> Formula:
    return ["fof(goal, conjecture, (? [X] : p(X)) => (! [Y] : p(Y)))."]

def q_bare_existential() -> Formula:
    return ["fof(goal, conjecture, ? [X] : ? [Y] : (p(X) & q(Y)))."]

def q_forall_contradiction() -> Formula:
    return ["fof(goal, conjecture, ! [X] : (p(X) & ~ p(X)))."]

def q_bare_exists_universal() -> Formula:
    return ["fof(goal, conjecture, ! [X] : ? [Y] : r(X,Y))."]


# ── Problem collection ───────────────────────────────────────────────────────

def collect_all() -> dict:
    problems: dict = {"easy": [], "medium": [], "hard": [], "expert": []}

    # ── EASY (~30) ──────────────────────────────────────────────────────────
    e = problems["easy"]

    # Provable (~18)
    for dist in [0, 1, 2]:
        e.append(chain_provable(2, dist))
        e.append(chain_provable(3, dist))
    for depth in [2, 3, 4]:
        e.append(syllogism_provable(depth))
    e += [q_flip_universal(), q_exists_implies(), q_drinker(),
          q_converse_barcan(), q_pelletier18(), q_russell_diagonal()]
    e += [trans_provable(2, 0), trans_provable(3, 0), trans_provable(3, 1)]

    # Unprovable (~14)
    e += [chain_unprovable(2, 0, 0), chain_unprovable(2, 0, 1)]
    e += [chain_unprovable(3, 0, 0), chain_unprovable(3, 0, 1), chain_unprovable(3, 0, 2)]
    for depth in [2, 3, 4]:
        e.append(syllogism_unprovable(depth))
    e += [q_barcan(), q_exists_to_forall(), q_bare_existential(), q_forall_contradiction()]
    e += [trans_unprovable(2), trans_unprovable(3)]

    # ── MEDIUM (~60) ────────────────────────────────────────────────────────
    m = problems["medium"]

    # Provable (~30)
    for length in [4, 5, 6]:
        for dist in [2, 3, 4, 5]:
            m.append(chain_provable(length, dist))          # 12
    for depth in range(3, 8):
        m.append(syllogism_provable(depth))                 # 5
    for steps in [4, 5, 6]:
        for dist in [2, 3, 4]:
            m.append(trans_provable(steps, dist))           # 9
    m.append(["fof(ax0, axiom, ! [X] : (a(X) => b(X))).",
              "fof(ax1, axiom, ! [X] : (b(X) => c(X))).",
              "fof(ax2, axiom, ! [X] : (c(X) => d(X))).",
              "fof(inst, axiom, a(x0)).",
              "fof(goal, conjecture, d(x0))."])
    m += [trans_provable(4, 5), trans_provable(5, 5), trans_provable(6, 5)]  # 3
                                                            # total: 12+5+9+1+3 = 30

    # Unprovable (~30)
    for length in [4, 5, 6]:
        for gap in [0, length // 2, length - 1]:
            m.append(chain_unprovable(length, 2, gap))      # 9
    for depth in range(3, 8):
        m.append(syllogism_unprovable(depth))               # 5
    for steps in [4, 5, 6]:
        m.append(trans_unprovable(steps))                   # 3
    m += [q_barcan(), q_exists_to_forall(), q_bare_existential(),
          q_forall_contradiction(), q_bare_exists_universal()]  # 5
    for length in [4, 5, 6]:
        for gap in [1, length - 2]:
            m.append(chain_unprovable(length, 3, gap))      # 6
    m += [chain_unprovable(4, 4, 2), chain_unprovable(5, 4, 3)]  # 2
                                                            # total: 9+5+3+5+6+2 = 30

    # ── HARD (~60) ──────────────────────────────────────────────────────────
    h = problems["hard"]

    # Provable (~30)
    for length in [7, 8, 9, 10]:
        for dist in [4, 6, 8]:
            h.append(chain_provable(length, dist))          # 12
    for depth in range(5, 9):
        h.append(syllogism_provable(depth))                 # 4
    for steps in [7, 8, 9]:
        for dist in [4, 6, 8]:
            h.append(trans_provable(steps, dist))           # 9
    for length in [7, 8, 9, 10]:
        h.append(chain_provable(length, 10))                # 4
    h.append(trans_provable(10, 4))                         # 1
                                                            # total: 12+4+9+4+1 = 30

    # Unprovable (~30)
    for length in [7, 8, 9, 10]:
        for gap in [0, length // 3, length // 2, length - 1]:
            h.append(chain_unprovable(length, 4, gap))      # 16
    for steps in [7, 8, 9, 10]:
        h.append(trans_unprovable(steps))                   # 4
    for depth in range(5, 9):
        h.append(syllogism_unprovable(depth))               # 4
    for length in [7, 8, 9]:
        for gap in [length // 4, 3 * length // 4]:
            h.append(chain_unprovable(length, 6, gap))      # 6
                                                            # total: 16+4+4+6 = 30

    # ── EXPERT (~50) ────────────────────────────────────────────────────────
    ex = problems["expert"]

    # Provable (~25)
    for length in [11, 12, 13, 14, 15]:
        for dist in [8, 10, 12]:
            ex.append(chain_provable(length, dist))         # 15
    for steps in [10, 11, 12, 13]:
        for dist in [6, 8]:
            ex.append(trans_provable(steps, dist))          # 8
    ex.append(chain_provable(15, 14))                       # 1
    ex.append(trans_provable(14, 8))                        # 1
                                                            # total: 15+8+1+1 = 25

    # Unprovable (~25)
    for length in [11, 12, 13, 14, 15]:
        for gap in [0, length // 4, length // 2, length - 2, length - 1]:
            ex.append(chain_unprovable(length, 6, gap))     # 25

    return problems


def main(output_dir: str = "AI_generated") -> None:
    os.makedirs(output_dir, exist_ok=True)
    for f in os.listdir(output_dir):
        if f.endswith('.p'):
            os.remove(os.path.join(output_dir, f))

    problems = collect_all()
    for tier, problem_list in problems.items():
        for n, formulae in enumerate(problem_list, start=1):
            write_problem(os.path.join(output_dir, f"{tier}_{n}.p"), formulae)
        print(f"Generated {len(problem_list)} problems for {tier}")


if __name__ == "__main__":
    main()
